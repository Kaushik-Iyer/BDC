    <h1 class="display-5 fw-bold">Why Your Donation Matters</h1>
    <div class="col-lg-9 mx-auto">
      <p class="lead mb-9">There are no substitutes for blood, platelets or plasma—they cannot be manufactured. Patients in need of blood or blood products must rely on donations from people like you. Those who are hesitant about donating blood for the first time often find that the donation process is easy and that saving lives is deeply gratifying.</p>
      <p class="lead mb-9"> One donation can save multiple lives. Here are the facts:</p>
      <ul>
 <li>Just <b>1 donation</b> can save up to <b>3 lives.</b></li>
 <li>The average red blood cell transfusion is <b>3 pints</b> (or 3 whole-blood donations).</li>
 <li>More than <b>1 million</b> people every year are diagnosed with cancer for the first time. Many of them will need blood—sometimes daily—during chemotherapy.</li>
 <li>More than <b>38,000 blood donations</b> are needed every day.</li>
 <li><b>Type O-negative</b> whole blood can be transfused to people with any blood type, but this type of blood is rare, and supplies of it are low.</li>
 <li><b>Type AB plasma</b> can be transfused to patients with all other blood types, but it's also in <b>short supply</b>.</li>
</ul>

